class Message(A2AModel):
    """A2A Message object - fundamental communication unit."""

    role: MessageRole
    message_id: str = Field(alias="messageId")
    parts: list[Part]
    context_id: str | None = Field(default=None, alias="contextId")
    task_id: str | None = Field(default=None, alias="taskId")
    reference_task_ids: list[str] | None = Field(default=None, alias="referenceTaskIds")
    extensions: list[str] | None = None
    metadata: dict[str, Any] | None = None
    kind: Literal["message"] = "message"


class Artifact(A2AModel):
    """Tangible output generated by a task."""

    artifact_id: str = Field(alias="artifactId")
    name: str | None = None
    description: str | None = None
    parts: list[Part]
    extensions: list[str] | None = None
    metadata: dict[str, Any] | None = None
    # Streaming fields
    append: bool | None = None
    last_chunk: bool | None = Field(default=None, alias="lastChunk")
    index: int | None = None


