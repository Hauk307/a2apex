class A2AModel(BaseModel):
    """Base model with common configuration."""

    model_config = ConfigDict(
        extra="allow",  # Allow extra fields for forward compatibility
        populate_by_name=True,
        str_strip_whitespace=True,
    )


# ═══════════════════════════════════════════════════════════════════════════════
# FILE TYPES
# ═══════════════════════════════════════════════════════════════════════════════


class FileWithUri(A2AModel):
    """File reference via URI."""

    uri: str
    name: str | None = None
    mime_type: str | None = Field(default=None, alias="mimeType")


class FileWithBytes(A2AModel):
    """File with inline base64 content."""

    bytes: str  # Base64 encoded
    name: str | None = None
    mime_type: str | None = Field(default=None, alias="mimeType")


FileContent = Union[FileWithUri, FileWithBytes]


# ═══════════════════════════════════════════════════════════════════════════════
# PART TYPES (Discriminated Union)
# ═══════════════════════════════════════════════════════════════════════════════


class TextPart(A2AModel):
    """Text content part."""

    kind: Literal["text"] = "text"
    text: str
    metadata: dict[str, Any] | None = None


class FilePart(A2AModel):
    """File content part."""

    kind: Literal["file"] = "file"
    file: FileWithUri | FileWithBytes
    metadata: dict[str, Any] | None = None


class DataPart(A2AModel):
    """Structured data part."""

    kind: Literal["data"] = "data"
    data: dict[str, Any]
    metadata: dict[str, Any] | None = None


Part = Annotated[
    Union[TextPart, FilePart, DataPart],
    Field(discriminator="kind"),
]


