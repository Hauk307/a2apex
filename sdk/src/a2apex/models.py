"""
A2A Protocol Pydantic Models

Complete type definitions for the A2A Protocol (v0.3).
All models use Pydantic v2 for validation and serialization.

Based on: a2a_protocol_deep_dive.md and official a2a-python SDK types.
"""

from __future__ import annotations

from datetime import datetime
from enum import Enum
from typing import Annotated, Any, Literal, Union

from pydantic import BaseModel, ConfigDict, Field, HttpUrl, field_validator


# ═══════════════════════════════════════════════════════════════════════════════
# ENUMS
# ═══════════════════════════════════════════════════════════════════════════════


class TaskState(str, Enum):
    """All valid A2A task states."""

    SUBMITTED = "submitted"
    WORKING = "working"
    INPUT_REQUIRED = "input-required"
    AUTH_REQUIRED = "auth-required"
    COMPLETED = "completed"
    FAILED = "failed"
    CANCELED = "canceled"
    REJECTED = "rejected"


class MessageRole(str, Enum):
    """Role of message sender."""

    USER = "user"
    AGENT = "agent"


class PartKind(str, Enum):
    """Discriminator for Part types."""

    TEXT = "text"
    FILE = "file"
    DATA = "data"


class Transport(str, Enum):
    """Supported transport protocols."""

    JSONRPC = "JSONRPC"
    GRPC = "GRPC"
    HTTP_JSON = "HTTP+JSON"


class SecuritySchemeType(str, Enum):
    """OpenAPI 3.0 security scheme types."""

    API_KEY = "apiKey"
    HTTP = "http"
    OAUTH2 = "oauth2"
    OPENID_CONNECT = "openIdConnect"
    MUTUAL_TLS = "mutualTLS"


class ApiKeyLocation(str, Enum):
    """Where to send API key."""

    HEADER = "header"
    QUERY = "query"
    COOKIE = "cookie"


