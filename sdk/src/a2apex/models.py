def is_terminal_state(state: TaskState | str) -> bool:
    """Check if a task state is terminal (no further transitions)."""
    if isinstance(state, str):
        try:
            state = TaskState(state)
        except ValueError:
            return False
    return state in {
        TaskState.COMPLETED,
        TaskState.FAILED,
        TaskState.CANCELED,
        TaskState.REJECTED,
    }


def is_interrupted_state(state: TaskState | str) -> bool:
    """Check if a task state is interrupted (waiting for input/auth)."""
    if isinstance(state, str):
        try:
            state = TaskState(state)
        except ValueError:
            return False
    return state in {TaskState.INPUT_REQUIRED, TaskState.AUTH_REQUIRED}


def create_text_message(
    text: str,
    role: MessageRole = MessageRole.USER,
    message_id: str | None = None,
    context_id: str | None = None,
    task_id: str | None = None,
) -> Message:
    """Create a simple text message."""
    import uuid

    return Message(
        role=role,
        messageId=message_id or str(uuid.uuid4()),
        parts=[TextPart(text=text)],
        contextId=context_id,
        taskId=task_id,
    )


def create_jsonrpc_request(
    method: str,
    params: dict[str, Any] | None = None,
    request_id: str | int | None = None,
) -> JsonRpcRequest:
    """Create a JSON-RPC 2.0 request."""
    import uuid

    return JsonRpcRequest(
        id=request_id or str(uuid.uuid4()),
        method=method,
        params=params,
    )
